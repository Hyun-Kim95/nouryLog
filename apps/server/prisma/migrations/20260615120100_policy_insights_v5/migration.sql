nouryLog 운영자(이하 "운영자")는 「개인정보 보호법」 제30조에 따라 정보주체의 개인정보를 보호하고 이와 관련한 고충을 신속하게 처리할 수 있도록 본 개인정보처리방침을 수립·공개합니다. 본 방침은 Google Play 데이터 보안(Data Safety) 항목 작성 시 참고할 수 있도록 정리되었습니다.

## 1. 개인정보의 처리 목적

운영자는 다음의 목적을 위하여 개인정보를 처리하며, 처리 목적이 변경되는 경우에는 사전에 고지합니다.

1. **회원 식별 및 인증** — 이메일/비밀번호 또는 소셜 로그인(Naver, Google, Kakao)을 통한 본인 확인 및 부정 이용 방지.
2. **서비스 제공** — 식단 기록, 영양 분석, **식단 인사이트**(회원 본인 기록의 집계·목표 대비 분석 및 규칙 기반 요약·주간·월간 리포트), OCR 기반 음식 인식(월간 무료 한도), 권장 섭취량 추천, 통계 시각화, 공지사항 및 문의 대응.
3. **광고 게재** — 무료 이용 회원에게 Google AdMob 기반 배너 광고 표시(광고 식별자 등은 광고 SDK·플랫폼 정책에 따름).
4. **고객 응대** — 1:1 문의 답변 및 분쟁 해결.
5. **서비스 개선 및 통계** — 일별·기간별 영양 통계 산출, 추천 알고리즘 품질 평가(개인을 식별할 수 없는 비식별·집계 형태).
6. **법적 의무 이행** — 관계 법령에 따른 보관·신고 의무 준수.

**현재** 인앱 구독·유료 결제를 제공하지 않으므로, 결제 수단 정보·구독 결제 내역은 수집하지 않습니다. 추후 유료 기능 도입 시 본 방침을 갱신합니다.

## 2. 처리하는 개인정보 항목

운영자는 서비스 제공을 위해 다음의 개인정보를 수집·이용합니다.

| 구분 | 항목 |
|---|---|
| 필수 | 이메일 주소, 비밀번호(`bcrypt` 단방향 해시) |
| 소셜 로그인 시 | 소셜 제공자가 전달하는 식별자 및 이메일(네이버/구글/카카오) |
