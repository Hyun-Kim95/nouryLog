위 5항의 수탁자는 글로벌 사업자로서 일부 데이터가 **미국 등 해외**에서 저장·처리될 수 있습니다. 이전 항목·목적·보유 기간은 위탁 내역과 동일하며, 서비스 가입·이용 시 국외 이전에 동의한 것으로 봅니다.

## 7. 정보주체의 권리·의무 및 행사 방법

1. 정보주체는 운영자에 대해 언제든지 개인정보 열람, 정정, 삭제, 처리정지, 동의 철회를 요청할 수 있습니다.
2. 권리 행사는 서비스 내 "문의하기" 화면 또는 설정 화면의 탈퇴 기능을 통해 신청할 수 있으며, 운영자는 **서비스 노출 중단** 등 필요한 조치를 지체 없이 합니다.
3. **삭제·탈퇴 요청**의 경우, 요청 접수 후 해당 정보는 **즉시 서비스에서 이용·제공되지 않습니다**. DB상 **완전 삭제(파기)** 는 제3조 제2항의 보존 기간(비활성화 후 1년)이 경과한 후 수행하며, 법령상 보관 의무가 있는 정보는 해당 기간 동안 분리 보관합니다.
4. 정정 요청이 완료될 때까지 해당 개인정보를 이용·제공하지 않습니다.
5. 만 14세 미만 아동의 경우 법정대리인이 권리를 대리 행사할 수 있습니다.

## 8. 개인정보의 파기 절차 및 방법

1. 보유기간 경과(제3조의 1년 보존 포함) 또는 처리 목적 달성 시 지체 없이 파기합니다.
2. 비활성화 후 1년이 경과한 데이터는 **정기 배치 작업**을 통해 데이터베이스에서 영구 삭제합니다.
3. 전자적 파일·레코드는 복구 불가능한 방법으로 삭제합니다.

## 9. 개인정보의 안전성 확보 조치

1. **관리적 조치** — 접근 권한 최소화, 정기 점검.
2. **기술적 조치** — TLS 암호화, JWT·리프레시 토큰, 비밀번호 `bcrypt` 해시, DB 접근 통제.
3. **물리적 조치** — 위탁 호스팅 사업자의 물리 보안 정책 준수.

## 10. 자동 수집 장치·광고 식별자

1. 운영자는 자동 로그인·설정 유지를 위해 모바일 앱 **보안 저장소**(Expo Secure Store 등) 및 웹 어드민 **쿠키/localStorage**를 사용합니다.
2. **Google AdMob** 배너 광고를 표시하며, Google Mobile Ads SDK가 **광고 ID(Android: AAID 등)** 및 기기·앱 사용 정보를 수집할 수 있습니다. 맞춤 광고 거부는 단말기 **설정 → Google → 광고** 또는 iOS **추적 제한** 등 OS 설정을 이용할 수 있습니다.
3. 거부 시에도 비맞춤 광고가 표시될 수 있으며, 일부 기능(자동 로그인 등)은 저장 거부 시 제한될 수 있습니다.

