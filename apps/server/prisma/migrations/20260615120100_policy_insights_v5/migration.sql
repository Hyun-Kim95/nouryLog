| 고객 응대 | 문의 제목·본문, 답변 내용 |

운영자는 만 14세 미만 아동의 개인정보를 의도적으로 수집하지 않습니다. 만 14세 미만임이 확인되는 경우 해당 계정과 데이터를 삭제합니다.

## 3. 개인정보의 처리 및 보유 기간

1. 운영자는 정보주체의 개인정보를 **회원 가입 시점부터** 탈퇴·비활성 처리 전까지 보유·이용합니다.
2. **탈퇴·비활성화·삭제 요청 처리 후**
   - **이용 중단**: 앱·관리 화면에서 해당 데이터는 **즉시 조회·이용할 수 없습니다**(처리 정지에 준하는 조치).
   - **DB 보존**: 복구 요청·분쟁·부정이용 대응·서비스 안정성을 위해, 비활성화 처리가 완료된 시점(서버에 기록된 비활성화 일시)부터 **1년** 동안 보관한 뒤 **복구 불가능한 방법으로 완전 삭제(파기)** 합니다.
   - **기준 시각**: 비활성화·탈퇴·자격 상실 처리가 완료된 시각입니다. 재활성화하는 경우 비활성화 일시는 초기화됩니다.
3. **적용 데이터**: 회원 계정·프로필·식단 기록, 음식 템플릿, 문의, 공지 등 서비스 데이터베이스에 저장된 이용 데이터에 적용합니다.
4. **법령상 보관 예외**(위 2항의 1년 보존과 별도로, 해당 기간 동안 분리 보관 후 파기):
   - 「전자상거래 등에서의 소비자 보호에 관한 법률」: 계약·청약철회, 대금결제, 소비자 불만·분쟁처리 기록 등(해당 거래 발생 시).
   - 「통신비밀보호법」: 로그인 기록 등 통신사실 확인자료 3개월.
   - **감사·운영 로그**: 개인정보와 분리하여 **5년** 보관 후 파기합니다.
5. **OCR용 사진 데이터**는 분석 완료 후 지체 없이 파기하는 것을 원칙으로 하며, 위 2항의 1년 보존 대상에서 **제외**합니다.

## 4. 개인정보의 제3자 제공

운영자는 정보주체의 동의, 법률의 특별한 규정 등 「개인정보 보호법」 제17조 및 제18조에 해당하는 경우 외에는 개인정보를 제3자에게 **판매·공유하지 않습니다.**

다만 OCR·광고·호스팅 등 **처리 위탁**(아래 5항) 과정에서 수탁자가 기술적으로 데이터를 처리할 수 있습니다.

