| 서비스 이용 데이터 | 식단·영양 입력값, 식사 시각, 음식 템플릿, 식단 인사이트용 집계·리포트 캐시(본인 기록 기반) |
| OCR 이용 시 | 영양성분표 **사진 이미지 데이터**(분석 요청 시 서버로 전송, Google Cloud Vision API로 텍스트 추출). 운영자는 OCR 목적으로 사진을 **영구 보관하지 않으며**, 분석 처리에 필요한 범위에서 일시 처리합니다. |
| 통계·로그 | 통계 산출물, 접속 일시, 마지막 로그인 시각 등 |
