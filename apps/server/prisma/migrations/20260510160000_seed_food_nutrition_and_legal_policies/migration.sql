운영자가 이용하는 호스팅·인증·결제 인프라는 글로벌 사업자(Railway, Google, Apple 등)에 의해 운영되어 일부 데이터가 미국 등 해외 서버에 저장·처리될 수 있습니다. 이전되는 항목, 일시 및 방법, 이전받는 자, 보유 기간은 위 5항의 위탁 내역과 동일하며, 정보주체는 본 방침을 통해 이전 사실에 동의한 것으로 간주됩니다.

## 7. 정보주체의 권리·의무 및 행사 방법

1. 정보주체는 운영자에 대해 언제든지 개인정보 열람, 정정, 삭제, 처리정지, 동의 철회를 요청할 수 있습니다.
2. 권리 행사는 서비스 내 "문의하기" 화면을 통해 신청할 수 있으며, 운영자는 지체 없이 조치합니다.
3. 정보주체가 개인정보의 오류 등에 대한 정정을 요청한 경우 운영자는 정정 또는 삭제를 완료할 때까지 해당 개인정보를 이용하거나 제공하지 않습니다.
4. 만 14세 미만 아동의 경우 법정대리인이 권리를 대리 행사할 수 있습니다.

## 8. 개인정보의 파기 절차 및 방법

1. 보유기간이 경과하거나 처리 목적이 달성된 개인정보는 지체 없이 파기합니다.
2. 전자적 파일 형태의 정보는 복구 및 재생이 불가능한 방법으로 영구 삭제하며, 종이 문서 형태의 정보는 분쇄기로 분쇄하거나 소각합니다.

## 9. 개인정보의 안전성 확보 조치

운영자는 정보주체의 개인정보를 안전하게 관리하기 위해 다음의 조치를 시행합니다.

1. **관리적 조치** — 개인정보 처리 직원 최소화, 정기적인 자체 점검.
2. **기술적 조치** — 모든 통신 구간에 TLS 적용, JWT 단기 만료 + 리프레시 토큰 회전, 비밀번호 `bcrypt` 단방향 해시 저장, 데이터베이스 접근 통제 및 감사 로그.
3. **물리적 조치** — 위탁 호스팅 사업자(Railway)의 데이터센터 물리 보안 정책 준수.

## 10. 자동 수집 장치의 설치·운영 및 거부에 관한 사항

1. 운영자는 서비스 이용 환경 유지 및 자동 로그인을 위해 모바일 앱에서 보안 저장소(`AsyncStorage`) 및 웹 어드민에서 쿠키/`localStorage`를 사용합니다.
