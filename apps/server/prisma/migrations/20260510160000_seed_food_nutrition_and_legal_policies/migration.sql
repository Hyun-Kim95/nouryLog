운영자는 정보주체의 동의, 법률의 특별한 규정 등 「개인정보 보호법」 제17조 및 제18조에 해당하는 경우 외에는 개인정보를 제3자에게 제공하지 않습니다. 현재 정기적으로 제3자에게 제공하는 개인정보는 없습니다.

