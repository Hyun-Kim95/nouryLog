1. 운영자는 다음 각 호에 해당하는 경우 서비스의 전부 또는 일부를 일시 중단할 수 있습니다.
   - 시스템 점검, 보수, 교체가 필요한 경우
   - 호스팅 사업자(Railway 등) 또는 OCR·광고 등 외부 API 장애가 발생한 경우
   - 천재지변, 정전, 국가비상사태 등 불가항력의 사유가 있는 경우
2. 운영자는 위 사유로 인한 서비스 중단으로 회원에게 발생한 손해에 대해 고의 또는 중대한 과실이 없는 한 책임지지 않습니다.

## 제9조 (이용자의 게시물)

1. 회원이 입력한 식단, 영양 정보, 음식 사진 등 콘텐츠의 저작권은 회원 본인에게 귀속됩니다.
