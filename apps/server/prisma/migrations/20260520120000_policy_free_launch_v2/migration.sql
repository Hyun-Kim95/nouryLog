3. **광고:** 무료 이용 중 일부 화면 하단에 제3자 배너 광고(Google AdMob)가 표시될 수 있습니다.
4. **유료 부가기능:** 인앱 구독·유료 결제는 **현재 제공하지 않습니다.** 추후 제공 시 사전 공지 후 본 약관을 갱신합니다.
5. 운영자는 서비스 품질 향상을 위해 필요 시 일부 기능을 추가, 변경, 종료할 수 있으며, 중요한 변경은 사전 공지합니다.

