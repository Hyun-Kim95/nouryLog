1. **현재** 서비스는 무료로 제공되며, Google Play·App Store를 통한 **인앱 구독·유료 결제는 제공하지 않습니다.**
2. 운영자는 추후 프리미엄 구독 등 유료 부가기능을 도입할 수 있으며, 도입 시 요금·혜택·환불은 해당 스토어 정책 및 별도 고지를 따릅니다.
3. 무료 이용 중 표시되는 광고는 Google AdMob 등 제3자 광고 네트워크를 통해 제공되며, 광고 내용에 대한 책임은 해당 광고주·네트워크에 있습니다.
4. 유료 부가기능을 제공하게 된 이후, 명백한 결함이 있는 경우 회원은 운영자에게 환불 또는 보상을 요청할 수 있으며, 운영자는 합리적인 기간 내에 검토합니다.

## 제11조 (이용자의 의무 및 금지행위)

회원은 다음 각 호의 행위를 하여서는 안 됩니다.

1. 타인의 계정 정보를 도용하거나 부정하게 사용하는 행위
2. 서비스의 정상적인 운영을 방해하는 행위(자동화된 도구 사용, 비정상적인 트래픽 발생, OCR 한도 우회 시도 등)
3. 운영자의 사전 동의 없이 서비스를 영리 목적으로 이용하는 행위
4. 서비스에서 얻은 정보를 운영자의 동의 없이 제3자에게 제공하거나 출판하는 행위
5. 기타 관련 법령에 위반되는 행위

## 제12조 (개인정보 보호)

1. 운영자는 관련 법령이 정하는 바에 따라 회원의 개인정보를 보호하기 위해 노력합니다.
2. 개인정보의 수집·이용·보관·파기 등 처리에 관한 사항은 별도의 "개인정보처리방침"에서 정하며, 서비스 내 정책 문서 화면 및 공개 URL에서 확인할 수 있습니다.

## 제13조 (분쟁 해결 및 관할)

1. 본 약관과 관련하여 운영자와 회원 간 분쟁이 발생한 경우 우선 상호 협의하여 해결합니다.
