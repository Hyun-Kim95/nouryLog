3. 운영자는 다음 각 호에 해당하는 콘텐츠를 사전 통보 없이 비공개 처리하거나 삭제할 수 있습니다.
   - 타인의 권리를 침해하거나 명예를 훼손하는 내용
   - 음란성, 폭력성, 차별적 표현 등 공서양속에 반하는 내용
   - 광고, 스팸, 영리 목적의 무단 게재
   - 기타 관련 법령에 위반되는 내용

