   - 타 이용자의 서비스 이용을 방해하거나 권리를 침해한 경우
   - 장기간 서비스를 이용하지 않는 경우
   - 기타 운영자가 합리적으로 판단하여 서비스 운영에 중대한 지장을 초래하는 경우
3. 비활성 처리 시 회원의 식단 데이터는 즉시 비공개로 전환되며, 별도 보관 의무가 있는 정보를 제외하고 합리적인 기간 내에 파기합니다.

## 제7조 (서비스의 제공)

