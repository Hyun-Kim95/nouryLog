## 7. 정보주체의 권리·의무 및 행사 방법

1. 정보주체는 운영자에 대해 언제든지 개인정보 열람, 정정, 삭제, 처리정지, 동의 철회를 요청할 수 있습니다.
2. 권리 행사는 서비스 내 "문의하기" 화면을 통해 신청할 수 있으며, 운영자는 지체 없이 조치합니다.
3. 정정·삭제 요청이 완료될 때까지 해당 개인정보를 이용·제공하지 않습니다.
4. 만 14세 미만 아동의 경우 법정대리인이 권리를 대리 행사할 수 있습니다.

## 8. 개인정보의 파기 절차 및 방법

1. 보유기간 경과 또는 처리 목적 달성 시 지체 없이 파기합니다.
2. 전자적 파일은 복구 불가능한 방법으로 삭제합니다.

## 9. 개인정보의 안전성 확보 조치

1. **관리적 조치** — 접근 권한 최소화, 정기 점검.
2. **기술적 조치** — TLS 암호화, JWT·리프레시 토큰, 비밀번호 `bcrypt` 해시, DB 접근 통제.
