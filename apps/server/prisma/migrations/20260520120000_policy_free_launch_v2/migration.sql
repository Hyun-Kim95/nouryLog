2. 운영자는 다음 각 호에 해당하는 경우 가입을 승낙하지 않거나 사후에 가입을 취소할 수 있습니다.
   - 타인의 명의를 도용한 경우
   - 허위 정보를 기재하거나 운영자가 요구하는 사항을 누락한 경우
