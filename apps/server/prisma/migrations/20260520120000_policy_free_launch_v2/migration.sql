   - 기타 본 약관 또는 관련 법령에 위배되는 사유가 있는 경우

## 제5조 (회원 정보의 관리)

1. 회원은 자신의 계정 정보(이메일, 비밀번호, 프로필)를 정확하게 관리할 책임이 있습니다.
2. 회원은 비밀번호를 타인에게 양도·대여할 수 없으며, 비밀번호 유출로 인한 모든 책임은 회원 본인에게 있습니다.
3. 회원은 서비스 내 프로필 화면에서 본인 정보를 수정할 수 있습니다.

